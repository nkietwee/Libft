#include"libft.h"

char	*ft_strdup(const char *str)
{
	char	*s;
	size_t	i;

	s = (char *)malloc((ft_strlen(str)+1) * sizeof(char));
	i = 0;
	if (!s)
		return (NULL);
	while (str[i] != '\0')
	{
		s[i] = str[i];
		i++;
	}
	s[i] = '\0';
	return (s);
}
